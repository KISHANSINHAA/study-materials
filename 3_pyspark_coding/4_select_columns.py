# PySpark program to select columns from a DataFrame

import os
import sys
os.environ['PYSPARK_PYTHON'] = sys.executable
os.environ['PYSPARK_DRIVER_PYTHON'] = sys.executable

from pyspark.sql import SparkSession
from pyspark.sql.functions import col

spark = SparkSession.builder.appName("SelectColumns").master("local[*]").getOrCreate()

data = [("Alice", 25, "NY"), ("Bob", 30, "CA")]
df = spark.createDataFrame(data, ["Name", "Age", "State"])

# Select columns using string names or col() objects
selected_df = df.select("Name", col("State").alias("Location"))

selected_df.show()
